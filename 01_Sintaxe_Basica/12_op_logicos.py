#!/usr/bin/env python3

# -*- coding:utf-8 -*-


# Operadores Booleanos, True ou False, 1 ou Zero

x = True
y = False
z = 1
w = 0

# Os operadores lógicos são os operadores and, or, e not.


# Conjunção - AND ou &
print('x AND y =>', x and y)
print('z & w =>', z & w)
print()

# Disjunção - OR ou |
print('x OR y =>', x or y)
print('z v w =>', z | w)

# Negação - NOT
print('not x is', not x)
print('Negação de y', not y)
print('Negação de z', not z)
print('Negação de w', not w)
print()
